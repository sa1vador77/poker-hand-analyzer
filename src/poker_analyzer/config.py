def _strip_inline_comment(value: str) -> str:
    """Срезает хвостовой inline-комментарий ``… # …`` (после пробела) у незакавыченного значения.

    ``.env.example`` документирует значения комментариями в ТОЙ ЖЕ строке и копируется в
    ``.env`` как есть. Без среза строковый ключ-диапазон тащил «# …» в ``parse_range`` → краш
    на старте (кириллица комментария как ранг карты); числовой ключ молча падал в дефолт.
    Срез ТОЛЬКО когда перед ``#`` стоит пробел — «#» вплотную к значению считаем его частью
    (у нас таких значений нет). Закавыченное значение берём целиком (комментарий внутри кавычек
    — часть строки)."""
    if value[:1] in ('"', "'"):
        return value
    for i in range(1, len(value)):
        if value[i] == "#" and value[i - 1].isspace():
            return value[:i].rstrip()
    return value


def _load_dotenv(path: Path) -> None:
    """Минимальный загрузчик ``.env`` (строки ``KEY=VALUE``, ``#`` — комментарий) в
    ``os.environ``. Уже заданные переменные окружения НЕ перетираем (приоритет у среды).
    Inline-комментарии (``KEY=VALUE  # …``) срезаются. Нет файла — молча выходим (дефолты)."""
    try:
        text = path.read_text(encoding="utf-8")
    except OSError:
        return
    for raw in text.splitlines():
        line = raw.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        key, _, value = line.partition("=")
        key = key.strip()
        value = _strip_inline_comment(value.strip()).strip().strip('"').strip("'")
        if key and key not in os.environ:
            os.environ[key] = value


_load_dotenv(PROJECT_ROOT / ".env")


def _env_str(key: str, default: str) -> str:
    """Строковая калибровочная константа из окружения или дефолт."""
    return os.environ.get(key, default)


def _env_float(key: str, default: float) -> float:
    """Число с плавающей точкой из окружения (битое значение → дефолт)."""
    try:
        return float(os.environ[key])
    except (KeyError, ValueError):
        return default


def _env_int(key: str, default: int) -> int:
    """Целое из окружения (битое значение → дефолт)."""
    try:
        return int(os.environ[key])
    except (KeyError, ValueError):
        return default


def _env_triple(key: str, default: tuple[float, float, float]) -> tuple[float, float, float]:
    """Тройка ``"a,b,c"`` из окружения (флоп/тёрн/ривер) или дефолт."""
    raw = os.environ.get(key)
    if raw is None:
        return default
    try:
        a, b, c = (float(p) for p in raw.split(","))
        return (a, b, c)
    except ValueError:
        return default


